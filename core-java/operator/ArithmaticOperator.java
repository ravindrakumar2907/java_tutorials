
public class ArithmaticOperator{
	// static or instance variable
	
	//method
	public static void main(String args[]){
		// local variable
		int a = 25; 
		int b = 5;
		
		System.out.println(" a + b : " + (a + b));
		System.out.println(" a - b : " + (a - b));
		System.out.println(" a * b : " + (a * b));
		System.out.println(" a / b : " + (a / b));
		System.out.println(" a % b : " + (a % b));
	}
}