public class FirstVariableDemo{
	
	public static void main(String args[]){
		System.out.println("This is variable demo");
		
		int class10 = 10;
		System.out.println("int variable " + class10);
		
		float percentage = 90.0f;
		System.out.println("float variable " + percentage);
		
		char blodGroup = 'A';
		System.out.println("char variable " + blodGroup);
		
		boolean flag = false;
		System.out.println("boolean variable " + flag);
		
		String name = "Rahul";
		System.out.println("boolean variable " + name);
		
		short dNumber = 129;
		System.out.println("dNumber variable " + dNumber);
	}

}